class Solution {
private:
    void printMatrix(vector<vector<int>> answer) {
        for (vector<int> row: answer) {
            printf("[ ");
            for (int e: row) {
                printf("%5d ", e);
            }
            printf(" ]\n");
        }
        printf("=============================\n");
    }

public:
    vector<vector<int>> generateMatrix(int n) {
        printf("n=%d\n", n);
        vector<vector<int>> answer(n, vector<int>(n, 0)); // empty matrix with 0 filled
        //çº¯è€ƒä»£ç ç†Ÿç»ƒåº¦
        int startx = 0, starty = 0; // å®šä¹‰æ¯å¾ªç¯ä¸€ä¸ªåœˆçš„èµ·å§‹ä½ç½®
        int loop = n / 2; // æ¯ä¸ªåœˆå¾ªç¯å‡ æ¬¡ï¼Œ n=3, loop=1ï¼Œ åªå¾ªç¯ä¸€åœˆï¼ŒçŸ©é˜µä¸­é—´çš„å€¼éœ€è¦å•ç‹¬å¤„ç†
        int mid = n / 2; // çŸ©é˜µä¸­é—´çš„ä½ç½®ï¼Œ n=3, ä¸­é—´çš„ä½ç½®å°±æ˜¯(1,1), nä¸º5ï¼Œ ä¸­é—´ä½ç½®ï¼ˆ2ï¼Œ2ï¼‰
        int count = 1; // ç”¨æ¥ç»™çŸ©é˜µä¸­æ¯ä¸€ä¸ªç©ºæ ¼èµ‹å€¼
        int offset = 1; // æ¯ä¸€åœˆå¾ªç¯ï¼Œéœ€è¦æ§åˆ¶æ¯ä¸€æ¡è¾¹éå†çš„é•¿åº¦
        int i, j;

        while (loop--) {
            printf("loop: %d\n", loop);

            i = startx;
            j = starty;

            // ä¸‹é¢4ä¸ª for å°±æ˜¯æ¨¡æ‹Ÿè½¬äº†ä¸€åœˆ
            printf("startx = %d, starty = %d\n", startx, starty);
            // æ¨¡æ‹Ÿå¡«å……ä¸Šè¡Œä»å·¦åˆ°å³ (å·¦é—­å³å¼€ï¼‰
            printf("1st row left to right:\n");
            for (j = starty; j < starty + n - offset; j++) { // 1-5 left->right (1st loop)
                answer[startx][j] = count++; // å…ˆè¢«èµ‹å€¼ä¸ºcountï¼Œç„¶åcount++
                printMatrix(answer);
            }

            // æ¨¡æ‹Ÿå¡«å……å³åˆ—ä»ä¸Šåˆ°ä¸‹ï¼ˆå·¦é—­å³å¼€ï¼‰
            printf("last col up to down:\n");
            for (i = startx; i < startx + n - offset; i++) { // 6-10 up->down (1st loop)
                answer[i][j] = count++;
                printMatrix(answer);
            }

            // æ¨¡æ‹Ÿå¡«å……ä¸‹è¡Œä»å³åˆ°å·¦ï¼ˆå·¦é—­å³å¼€ï¼‰
            printf("last row right to left:\n");
            for (; j > starty; j--) { // 11-15 right->left (1st loop)
                answer[i][j] = count++;
                printMatrix(answer);
            }

            // æ¨¡æ‹Ÿå¡«å……å·¦åˆ—ä»ä¸‹åˆ°ä¸Šï¼ˆå·¦é—­å³å¼€ï¼‰
            printf("first col down to up:\n");
            for (; i > startx; i--) { // 17-20 down->up (1st loop)
                answer[i][j] = count++;
                printMatrix(answer);
            }

            // ç¬¬äºŒåœˆå¼€å§‹çš„æ—¶å€™ï¼Œ èµ·å§‹ä½ç½®è¦å„è‡ªåŠ 1ï¼Œ eg ç¬¬ä¸€åœˆèµ·å§‹ä½ç½®ï¼ˆ0ï¼Œ0ï¼‰ï¼Œç¬¬äºŒåœˆèµ·å§‹ä½ç½®ä¸ºï¼ˆ1ï¼Œ1ï¼‰
            printf("loop end, go to inner circle\n");
            startx++; // change start index x coordinate
            starty++; // change start index y coordinate
            printf("start point are going inner diagonally\n");

            // offset æ§åˆ¶æ¯ä¸€åœˆæ¯ä¸€æ¡è¾¹éå†çš„é•¿åº¦
            offset += 2;

        }

        // å¦‚æœnä¸ºå¥‡æ•°ï¼Œ éœ€è¦å•ç‹¬ç»™çŸ©é˜µæœ€ä¸­é—´çš„ä½ç½®èµ‹å€¼
        if (n % 2) {
            answer[mid][mid] = count;
            printMatrix(answer);
        }

        printMatrix(answer);
        return answer;
    }
};

