class Solution:
    def climbStairs(self, n: int) -> int:
        dp = [0, 1, 2]
        i = len(dp) - 1
        while n > len(dp) - 1:
            dp.append(dp[i] + dp[i - 1])
            i += 1
        return dp[n]


